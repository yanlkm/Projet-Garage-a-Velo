package elements;

/**
 * Représente la marque d'un vélo.
 */
public class Marque {
  
  /**
  * L'attribut de chaine de caractère indique la marque du vélo.
  */
  protected String marque;

  public Marque(String marque) {
    this.marque = marque;
  }

  public String getMarque() {
    return marque;
  }

  public void setMarque(String marque) {
    this.marque = marque;
  }
  
  
 
}
